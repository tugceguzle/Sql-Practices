--Sütunların her birine göre diğer sütunları güncelleyecek 5 adet UPDATE işlemi yapalım.
--1)Email adresi .com ile biten kullanıcıların email adresini "email = 'email_bulunamadi'" olarak değiştir.
UPDATE employee
SET email = 'email_bulunamadi'
WHERE email ILIKE '%.com'
RETURNING * ;

--2)Id'si 1'den büyük eşit 5'ten küçük eşit (1<= id <=5) olan kullanıcıların doğum tarihini 20 temmuz 2003 olarak değiştir.
UPDATE employee
SET birthday = '20-07-2003'
WHERE id BETWEEN 1 AND 5
RETURNING * ;

--3)Adı 'Natalie Blogg' olan kullanıcının adını Tuğçe Güzle ve doğum tarihini 20 Temmuz 2003 olarak değiştir.
UPDATE employee
SET name = 'Tuğçe Güzle' , birthday ='20-07-2003'
WHERE name = 'Natalie Blogg'
RETURNING * ;

--4)Doğum tarihi 20 Temmuz 2003 olan kullanıcılardan id=14 olan kullanıcı hariç doğum tarihini 15 Haziran 2000 olarak değiştir.
UPDATE employee
SET birthday = '15-06-2000'
WHERE birthday = '20-07-2003' AND NOT id = 14
RETURNING * ;

--5)Adı 'Sayre Till' olan ya da email adresi 'kbolandp@g.co' olan kullanıcıların adını 'BOT' olarak değiştir.
UPDATE employee
SET name = 'BOT'
WHERE name = 'Sayre Till' OR email = 'kbolandp@g.co'
RETURNING * ;




